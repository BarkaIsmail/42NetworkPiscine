#include<stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb == 1)
		return (1);
	if (nb > 0)
	{
		while (i < nb && i <= 46340)
		{
			if ((i * i) == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
