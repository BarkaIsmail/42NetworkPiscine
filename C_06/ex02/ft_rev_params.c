#include<unistd.h>

void	ft_putstr(char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
}

int	main(int ac, char **av)
{
	int	i ;

	i = 1;
	while (ac > i)
	{
		ft_putstr(av[ac - 1]);
		write (1, "\n", 1);
		ac--;
	}
}
